    public Tipo ConfirmaTipo(int posicion){
        return switch (posicion) {
            case 0 -> Tipo.FUEGO;
            case 1 -> Tipo.AIRE;
            case 2 -> Tipo.AGUA;
            case 3 -> Tipo.MAGIABLANCA;
            case 4 -> Tipo.MAGIANEGRA;
            case 5 -> Tipo.ELECTRICIDAD;
            case 6 -> Tipo.HIELO;
            case 7 -> Tipo.ACIDO;
            case 8 -> Tipo.ESPIRITUAL;
            case 9 -> Tipo.HIERRO;
            default -> null;
        };
    }
